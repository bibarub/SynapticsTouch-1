#include <wdm.h>

#define Trace(Level, Flags, Msg, ...) \
			DbgPrintEx(DPFLTR_IHVDRIVER_ID, DPFLTR_ERROR_LEVEL, "ST: %s (%i) " Msg "\n", Level, Flags, __VA_ARGS__);

