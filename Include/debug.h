typedef enum _TraceFlags
{
    TRACE_FLAG_INIT = 1,
    TRACE_FLAG_REGISTRY,
    TRACE_FLAG_HID,
    TRACE_FLAG_PNP,
    TRACE_FLAG_POWER,
    TRACE_FLAG_SPB,
    TRACE_FLAG_CONFIG,
    TRACE_FLAG_REPORTING,
    TRACE_FLAG_INTERRUPT,
    TRACE_FLAG_SAMPLES,
    TRACE_FLAG_OTHER,
    TRACE_FLAG_IDLE,
    TRACE_FLAG_DRIVER
} TraceFlag;

